// Functions for arithmetic operations add, subtract, multiply, and divide
// Add
var add = (num1, num2) => {
	return num1 + num2;
};

// Subtract
var subtract = (num1, num2) => {
	return num1 - num2;
};

// multiply
var multiply = (num1, num2) => {
	return num1 * num2;
};

// divide
var divide = (num1, num2) => {
	return num1 / num2;
};






